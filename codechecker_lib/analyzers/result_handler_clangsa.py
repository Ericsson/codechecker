"""
result handlers for the Clang Static Analyzer
"""

from codechecker_lib.analyzers.result_handler_plist_to_db import PlistToDB
from codechecker_lib.analyzers.result_handler_plist_to_stdout import PlistToStdout


class ClangSAPlistToDB(PlistToDB):
    """
    Store the result plist generated by the Clang Static Analyzer
    to the database
    """

    def postprocess_result(self):
        """
        No postprocessing required for clang static analyzer
        """
        pass


class ClangSAPlistToStdout(PlistToStdout):
    """
    Print the results from the generated plist file to the standard output
    """

    def postprocess_result(self):
        """
        No postprocessing required for clang static analyzer
        """
        pass
